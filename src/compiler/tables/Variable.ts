export class Variable {
  //------------------------------- Class Variables --------------------------------//

  public name: string;
  public addPile: number = -1;
  public scope: number = -1;
  public type: string;
  public isParameter: boolean;

  //--------------------------------------------------------------------------------//

  //--------------------------------- Constructor ----------------------------------//

  constructor(name: string, type: string, isParameter: boolean, addPile?: number, scope?: number ) {
    this.name = name;
    this.type = type;
    if (!(addPile === undefined)) {
      this.addPile = addPile;
    }
    if (!(scope === undefined)) {
      this.scope = scope;
    }
    this.isParameter = isParameter;
  }

  //--------------------------------------------------------------------------------//

  //----------------------------------- Methods ------------------------------------//
  //
  //--------------------------------------------------------------------------------//
}
