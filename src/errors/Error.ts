import * as vscode from "vscode"

//--------------------------------------------------------------------------------//

export class Error {

//------------------------------- Class Variables --------------------------------//

  private line: number;
  private message: String;
  private output : vscode.OutputChannel;

//--------------------------------------------------------------------------------//

//--------------------------------- Constructor ----------------------------------//

  constructor(output : vscode.OutputChannel, line: number, message: string) {

    this.line = line;
	this.message = message;
	this.output = output;
  }
//--------------------------------------------------------------------------------//

//----------------------------------- Methods ------------------------------------//
  

  public display(){
	this.output.appendLine("ERROR at line "+this.line+" : "+this.message);
  }
//--------------------------------------------------------------------------------//
}
